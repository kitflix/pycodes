# i starts from 0, will increment by +1 stop at the range
for i in range(3):
     print("My name is Amit, value of i = ",i)

for j in range(6,10):
     print ("Value of j = ",j)

for z in range(10,50,5):
     print("Value of z = ",z)


for y in range(100,10,-10):
     print("value of y = ",y)
